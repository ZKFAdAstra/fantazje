<p>
    Tak się stało, że nasze miasto niewiele otrzymało w prezencie od historii. 
    Nie ma majestatycznych budowli, w cieniu których wypoczywają matki z wózkami. 
    Nie ma zamku, katedry, pałacu, kratowego kręgosłupa mostu przerzuconego nad szeroką rzeką. 
    Uniwersytet nie został ufundowany 100 lat temu. Nie ma tajemniczych lochów 
    metra z zapomnianymi tunelami. Jeszcze pół wieku temu byliśmy małym, 30-tysięcznym miasteczkiem.
</p>
<p>    
    A przecież toczyło się tu wcześniej życie, wypowiadane w innych językach. 
    Śmiano się i umierano, produkowano wino, wydawano gazety, kuto mosty dla 
    innych miast. Działy się historie i Historie. Były zarazy, pożary całego 
    miasta, czarownice i przemarsze chyba każdej armii...
</p>
<p>    
    Dlatego z tego co było, co być mogło, co być może, a nawet z tego, co nigdy 
    się nie wydarzy, zapragnęliśmy stworzyć nieco naszej miejskiej mitologii, 
    stworzyć historie naszego Macondo. Myśleliśmy o tym od lat, lecz zamiar 
    wydawał się wyższy od nas o głowę. Próbowaliśmy zarazić pomysłem innych, 
    lecz w końcu machnęliśmy na wszystko ręką i rzuciliśmy się naprzód. Bo któż, jeśli nie my? 
</p>
<p>    
    Tak oto w 2011 roku rozpoczęliśmy nasz cykl „Fantazji Zielonogórskich”. 
    Igor Myszkiewicz, klubowy grafik, stworzył niesamowite ilustracje. Mamert 
    Janion nocami poskładał teksty. Ania Cejmer-Kamińska zrobiła zawodową korektę. 
    Maciej Dobrowolski wspaniale sekretarzował konkursowi. Wszystkim należą się 
    ogromne podziękowania, bo czyż trzeba dodawać, że nikt nie pracował tu za choćby 
    złotówkę? Osobne podziękowania składam osobom, które zgodziły się zasiąść 
    w jury i swoim autorytetem uwiarygodnić nasz konkurs: bez Krzysztofa Koziołka, 
    znanego pisarza regionalisty; prof. dr. hab. Bogdana Trochy  z Uniwersytetu 
    Zielonogórskiego; wspierającego lokalne inicjatywy redaktora Eugeniusza Kurzawy 
    z „Gazety Lubuskiej” oraz Kazika Kielarskiego, naszego wieloletniego prezesa 
    rzecz na pewno by się nie udała.
</p>
<p>    
    Czasem myślę, że żyjemy w mieście podwójnym. Chodzimy po deptaku, wśród 
    cukierkowych kamienic, wokół Ratusza, zaglądamy w witryny naszego miasta i 
    nie wiemy, że pod naszymi stopami płynie smolista rzeka czasu przeszłego, 
    nieopowiedzianych historii. Zapomniany Grünberg, Zielona Góra niedokonana.
</p>
<p>    
    Na pewno po jej falach płynie nasz wspaniały klubowy kolega Jerzy Sulisławski. 
    Człowiek wielkiej wiedzy i jeszcze większego serca. Choć jest w naszej pamięci, 
    brak go nam na co dzień. Jerzy, Tobie dedykujemy nasze dzieło...
</p>
<p>    
    Państwa zapraszam do pisania kolejnych opowiadań do III edycji „Fantazji”. 
    Na tej stronie znajdziecie wszelkie informacje o konkursie. W tym roku nasz 
    konkurs został objęty wsparciem finansowym władz miasta, za co dziękujemy. 
    Wierzę, że robimy coś wspólnie z pożytkiem dla tego naszego miejsca na Ziemi. 
    Zaś Czytelników zachęcam do czytania pierwszych dwóch tomów. Oto Zielona Góra, 
    jakiej się nie spodziewacie. Jest tuż obok Was.
</p>
