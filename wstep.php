<p>
    Zapraszam Was już po raz szósty do wypełnienia opowieściami nowego tomu Fantazji Zielonogórskich.
    Historia Zielonej góry to nieprzebrane źródło inspiracji i tematów, wciąż odkrywanych i tych dobrze już znanych.
    W naszym mieście działy się rzeczy przedziwne, straszne, piękne i te całkiem zwyczajne choć nie mniej pasjonujące.
    Czasami gościła tu także ta Wielka Historia. Toczyło się życie, wypowiadane w innych językach. Śmiano się i umierano,
    produkowano wino, wydawano gazety, kuto mosty dla innych miast. Były zarazy, pożary całego miasta, czarownice i
    przemarsze chyba każdej armii, płynęła rzeka po której dziś nie ma już śladu...
</p>
<p>
    Dlatego z tego co było, co być mogło, co być może, a nawet z tego, co nigdy się nie wydarzy, zapragnęliśmy stworzyć
    nieco naszej miejskiej mitologii, stworzyć historie naszego Macondo. Myśleliśmy o tym od lat, lecz zamiar wydawał
    się wyższy od nas o głowę. Próbowaliśmy zarazić pomysłem innych, lecz w końcu machnęliśmy na wszystko ręką i
    rzuciliśmy się naprzód. Bo któż, jeśli nie my?
</p>
<p>
    Tak oto w 2011 roku rozpoczęliśmy nasz cykl „Fantazji Zielonogórskich”. Igor Myszkiewicz, klubowy grafik, stworzył
    niesamowite ilustracje. Mamert Janion nocami poskładał teksty. Ania Cejmer-Kamińska i Arlena A. Łobodzińska robiły
    zawodową korektę. Maciej Dobrowolski wspaniale sekretarzował konkursowi. Wszystkim należą się ogromne podziękowania,
    bo czyż trzeba dodawać, że nikt nie pracował tu za choćby złotówkę?
</p>
<p>
    Czasem myślę, że żyjemy w mieście podwójnym. Chodzimy po deptaku, wśród cukierkowych kamienic, wokół Ratusza,
    zaglądamy w witryny naszego miasta i nie wiemy, że pod naszymi stopami płynie smolista rzeka czasu przeszłego,
    nieopowiedzianych historii. Zapomniany Grünberg, Zielona Góra niedokonana.
</p>
<p>
    Na pewno po jej falach płynie nasz wspaniały klubowy kolega Jerzy Sulisławski. Człowiek wielkiej wiedzy i jeszcze
    większego serca. Choć jest w naszej pamięci, brak go nam na co dzień. Jerzy, Tobie dedykujemy nasze dzieło...
</p>
