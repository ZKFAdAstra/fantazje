    Nadchodzi czas Fantazji!
</p>
<p>
    Zielonogórski Klub Fantastyki Ad Astra ogłasza VI edycję otwartego konkursu
    literackiego FANTAZJE ZIELONOGÓRSKIE na opowiadanie fantastyczne inspirowane przeszłością, teraźniejszością
    lub przyszłością Zielonej Góry. Prace powinny być osadzone w konwencji szeroko pojmowanej fantastyki
    (sci-fi, fantasy, horror, legendy miejskie, realizm magiczny, itp.) splecionej z wątkami zielonogórskiej
    historii lub codzienności. Najlepsze konkursowe opowiadania zebrane zostaną w postaci antologii.
</p>
<p>
    Sądzimy, że wiedzę o Zielonej Górze i jej bogatej historii można upowszechniać również za pomocą fantastyki.
    Klimat i metafizykę miasta można budować także w świecie wyobraźni. Chcielibyśmy, aby co roku o kolejny tomik
    rozrastała się biblioteczka zielonogórskich fantazji, marzeń i koszmarów. Na półkach czytelników stoi już pięć
    tomów tekstów o naszym fantastycznym mieście. Zaś w ub. roku „Fantazje” zwyciężyły w plebiscycie NASZE DOBRE LUBUSKIE.
</p>
<p>
    Konkurs towarzyszy tegorocznym jubileuszowym <a href="http://www.bachanalia.zgora.pl" target="_new">30. Bachanaliom Fantastycznym</a>,
    które odbędą się we wrześniu 2016 roku w Zielonej Górze. Wtedy też ukaże się antologia z najlepszymi tekstami.
    Zwycięzca naszego konkursu, prócz publikacji i chwały, otrzyma też nagrodę finansową w wysokości 1000 zł.
</p>
<p>
