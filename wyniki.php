<p>
Z wielką przyjemnością możemy ogłosić, iż zwycięzcą w 2015 roku został <strong>Marek Starosta</strong>, który w swoim opowiadaniu <em>„Bóg, który przychodzi”</em>, urzekł kapitułę świetną klimatyczną historią osadzoną w latach 30. ubiegłego wieku. Tajne kulty, Bachus, polski ksiądz oraz niemiecka agentka, ten tekst po prostu porywa czytelnika i zabiera w niezapomnianą przygodę.
</p> 
<p>
Panu Markowi serdecznie dziękujemy, szczególnie iż jest autorem, którego teksty można przeczytać w poprzednich edycjach Fantazji Zielonogórskich.  Zwycięzca otrzyma od nas główną nagrodę o wartości tysiąca złotych. Gratulujemy!
</p>
<p>
Jednocześnie kapituła wyróżniła dwie prace, które jej zdaniem były również bardzo ciekawe i stanowiły nie małą konkurencję dla zwycięzcy. Jest to <em>„Król Szczupak”</em> napisany przez <strong>Monikę Kasprowiak </strong> oraz <em>„Grünberg.One”</em>, którego autorem jest <strong>Adam Froń</strong>.
</p>
<p>
Pozostałe prace, które ze względu na swój wysoki poziom znajdą się w książkowej publikacji naszej antologii konkursowej to:<br/> 
<em>"Miasto Srok"</em> - Dominika Hejduk, <em>"Saudade Pana"</em> - Filip Koziński, <em>"Zbieracz Pijanych Hansów"</em> - Krzysztof Gniady, <em>"Było jutro"</em> - Marta Magdalena Lasik, <em>"Af Rika"</em> - Marek Zychla, <em>"Nihil fit sine causa"</em> - Dorota Dziedzic-Chojnacka, <em>"Czego należało dowieść"</em> - Marta Krajewska, <em>"Sroki z Wieży Głodowej"</em> - Alicja Tempłowicz oraz <em>"Familariusz" </em>- Marek Palutkiewicz
</p>
