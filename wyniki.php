<h2>Znamy zwycięzcę III edycji konkursu Fantazje Zielonogórskie!</h2>

<p>
	Zielonogórski Klub Fantastyki Ad Astra ogłosił w tym roku III edycję
	otwartego konkursu literackiego Fantazje Zielonogórskie na opowiadanie
	fantastyczne inspirowane przeszłością, teraźniejszością lub
	przyszłością Zielonej Góry. Zainteresowanie naszą inicjatywą było
	bardzo duże, łącznie otrzymaliśmy 62 opowiadania nadesłane z całej
	Polski. Jury konkursowe miało ciężki orzech do zgryzienia, bowiem wiele
	praca prezentowało naprawdę wysoki poziom. Z tym większą
	przyjemnością chciałbym, zatem ogłosić, iż kapituła oceniająca pod
	długich i burzliwych obradach wyłoniła zwycięzcę. Został nim <strong>Michał
	Rybiński</strong>, który swoim opowiadaniem <em>„Dzieci i psy”</em>, utrzymanym w
	ciężkim postapokaliptycznym klimacie, zachwycił członków jury. Pan
	Michał oprócz lauru zwycięzcy otrzyma główną nagrodę o wartości 700
	złotych. Gratulujemy!
</p>
<p>
	Jednocześnie kapituła wyróżniła kilkanaście prac, które jej zdaniem
	były również bardzo ciekawe i zasługują na uwagę: <em>„Wahadło
	Igora”</em> - Krzysztof Nowecki, <em>„Kołyska Judasza”</em> - Andriej Kotin,
	<em>„Drugi”</em> - Joanna Pawłusiów, <em>„Diabeł na wirażu”</em> - Marek
	Starosta, <em>„Lampa”</em> - Dariusz Gruszecki, <em>„Pod maską”</em> - Zuzanna
	Nowicka, <em>„Leszy”</em> - Filip Kopij, <em>„Kawalerka z widokiem do
	wynajęcia”</em> - Anna Rosner, <em>„Dar od Boga”</em> - Maciej Kaźmierczak, <em>„Na
	głębokość skóry”</em> - Char Morana, <em>„Wino musujące”</em> – Alfred
	Siatecki, <em>„Wampiry Grunbergu”</em> - Waldemar Konieczny, <em>„Rynek”</em> –
	Czesław Markiewicz, <em>„Duszek historii”</em> - Krzysztof Filipowicz.
</p>
<p>
	Wszystkie wymienione opowiadania, wraz z pracą zwycięzcy, znajdą w
	książkowym wydaniu antologii konkursowej pt. Fantazje Zielonogórskie tom
	III. Publikacja swoją premierę będzie miała podczas tegorocznych
	Bachanaliów Fantastycznych odbywających się w dniach 27-29 września w
	Zielonej Górze. Wtedy też wręczone zostaną wyróżnienia oraz nagroda
	główna. W imieniu ZKF Ad Astra chciałbym serdecznie podziękować
	wszystkim uczestnikom naszej zabawy za udział w&nbsp;konkursie. 
</p>
