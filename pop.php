<h2>Fantazje Zielonogórskie</h2>
<h3>Antologia opowiadań fantastycznych</h3>

<p>
    Antologia opowiadań fantastycznych inspirowanych przeszłością, teraźniejszością 
    bądź przyszłością Zielonej Góry jest pierwszym tego typu wydawnictwem w historii 
    naszego Miasta. Celem publikacji, poza dostarczeniem czytelnikowi pasjonującej lektury, 
    jest także promocja wiedzy o Zielonej Górze, wspólne budowanie zielonogórskiej metafizyki, 
    obrazu Miasta w sferze fikcji literackiej… Co roku powstaje kolejny tom zielonogórskich fantazji, 
    marzeń i koszmarów.
</p>
<p>
    Antologia poświęcona jest pamięci Jerzego Sulisławskiego, znawcy i pasjonata 
    literatury fantastycznej, członka ZKF Ad Astra.
</p>
<p>
    Organizatorem konkursu i wydawcą antologii jest Zielonogórski Klub Fantastyki Ad Astra.
</p>

<img class="okladka" src="img/fz1.gif" title="tom 1" alt="tom 1, okładka" />
<h3 class="tom">Tom 1</h3>
<p>
    Wiosną 2011 r. Zielonogórski Klub Fantastyki Ad Astra ogłosił ogólnopolski 
    konkurs literacki na opowiadanie fantastyczne o tematyce zielonogórskiej. 
    Napłynęło 40 prac z całego kraju. Wyboru dokonało jury w składzie: dr Bogdan 
    Trocha (Uniwersytet Zielonogórski), Krzysztof Koziołek (pisarz) i Maciej Dobrowolski 
    (Gazeta Lubuska). Spośród ponad 40 nadesłanych z całej Polski prac w książce 
    znalazły się następujące utwory:
</p>
<ul>
    <li>Monika Glibowska „Śmierć zielonego jokera”</li>
    <li>Magdalena Herbert „Dziedzictwo”</li>
    <li>Małgorzata Kobylak (utwór bez tytułu)</li>
    <li>Czesław Markiewicz „Jaskółcza - Ogrodowa Brunatny zapach kamfory”</li> 
    <li>Igor Myszkiewicz „Bezpowrotnie”</li>
    <li>Robert Narkun „Dziedzictwo Beuchelta”</li>
    <li>Michał Organiściak „Pradzieje Góry Zielonej”</li>
    <li>Konrad Rataj „Nie jestem stąd”</li>
    <li>Alfred Siatecki „Będzie koniec świata w Grünbergu”</li>
    <li>Sławek Stawarczyk „Podróż”</li>
    <li>Andrzej Janion „Baśń o starym winie” - fragment dramatu o powstaniu miasta Zielonej Góry</li>
</ul>
<p>
    Nagrodę Grand Prix zdobyło opowiadanie Małgorzaty Kobylak <a href="pliki/Malgorzata-Kobylak-bez-tytulu.pdf" target="_new">(przeczytaj opowiadanie)</a>.
</p>
<p>
    Projekt okładki i ilustracje do umieszczonych w antologii utworów wykonał Igor Myszkiewicz.
</p>
<p>
    Zamieszczone w tomie opowiadania reprezentują pełną paletę konwencji szeroko rozumianej fantastyki – od klasycznej science fiction, fantasy, horroru aż po realizm magiczny.
</p>
<p>
    Książkę wydano przy współpracy Fabryki Reklamy IBI i Galerii Twórców Galera. 
</p>
<p>
    Patronat medialny: Gazeta Lubuska
</p>

