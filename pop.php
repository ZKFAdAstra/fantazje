<h2>Fantazje Zielonogórskie</h2>
<h3>Antologia opowiadań fantastycznych</h3>

<p>
    Antologia opowiadań fantastycznych inspirowanych przeszłością, teraźniejszością 
    bądź przyszłością Zielonej Góry jest pierwszym tego typu wydawnictwem w historii 
    naszego Miasta. Celem publikacji, poza dostarczeniem czytelnikowi pasjonującej lektury, 
    jest także promocja wiedzy o Zielonej Górze, wspólne budowanie zielonogórskiej metafizyki, 
    obrazu Miasta w sferze fikcji literackiej… Co roku powstaje kolejny tom zielonogórskich fantazji, 
    marzeń i koszmarów.
</p>
<p>
    Antologia poświęcona jest pamięci Jerzego Sulisławskiego, znawcy i pasjonata 
    literatury fantastycznej, członka ZKF Ad Astra.
</p>
<p>
    Organizatorem konkursu i wydawcą antologii jest Zielonogórski Klub Fantastyki Ad Astra.
</p>

